# print("백문이 불여일견\n백견이 불여일타")


# \" \' : 문장 내에서 따옴표
print("저는 \"Costar011\" 입니다.") 
print("저는 \'Costar011\' 입니다.")

# \\ : 문장 내에서 \
print("\\Coding\\YR\\python_deeplearning") # /Coding/YR/python_deeplearning

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine") # PineApple

# \b : 백스페이스(delete)  (한 글자 삭제)
print("Redd\bApple") # RedApple

# \t : 탭(tap)
print("Red\tApple") # Red    Apple