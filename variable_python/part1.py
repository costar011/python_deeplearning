# 곱하거나 나누기 예시
a = 50
b = 40

result = a / b
print(result)

result = a // b  # 나눴을 때 몫.
print(result)

result = a % b  # 나누었을 때 나머지.
print(result)

# 변수에는 정수뿐만 아니라 실수나 문자도 넣을 수 있다.
